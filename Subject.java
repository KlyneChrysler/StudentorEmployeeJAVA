class Subject {
    private String subjectName;
    private double grade;

    void setSubjectName(String subjectName){
        this.subjectName = subjectName;
    }

    void setGrade(double grade){
        this.grade = grade;
    }

    String getSubjectName(){
        return subjectName;
    }

    double getGrade(){
        return grade;
    }
}